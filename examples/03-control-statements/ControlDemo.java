// ControlDemo.java
// Demonstrates if-else and for loop in Java.

public class ControlDemo {
	public static void main(String[] args) {
		// If-else statement
		int number = 5;
		if (number > 0) {
			// Positive number
			System.out.println("Positive number");
		} else {
			// Non-positive number
			System.out.println("Non-positive number");
		}

		// For loop
		for (int i = 1; i <= 3; i++) {
			// Print i
			System.out.println("i = " + i);
		}
	}
}
