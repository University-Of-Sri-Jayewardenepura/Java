// DataTypesDemo.java
// Demonstrates primitive and non-primitive data types in Java.

public class DataTypesDemo {
	public static void main(String[] args) {
		// Integer data type
		int age = 20;

		// Double data type (decimal numbers)
		double salary = 35000.50;

		// Character data type
		char grade = 'A';

		// Boolean data type
		boolean isStudent = true;

		// String (non-primitive data type)
		String name = "Alice";

		// Print all variables
		System.out.println("Name: " + name + ", Age: " + age + ", Salary: " + salary + ", Grade: " + grade + ", Student: " + isStudent);
	}
}
